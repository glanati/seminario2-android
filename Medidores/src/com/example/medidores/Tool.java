package com.example.medidores;

public class Tool {
	
	private String titulo;
	private String sub;
	
	
	
	
	
	public Tool(String titulo, String sub) {

		this.titulo = titulo;
		this.sub = sub;
	}





	public String getTitulo() {
		return titulo;
	}





	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}





	public String getSub() {
		return sub;
	}





	public void setSub(String sub) {
		this.sub = sub;
	}
	
	
	

}
