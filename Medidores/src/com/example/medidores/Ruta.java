package com.example.medidores;

public class Ruta {
	
	private int ruta;
	private String calle;
	
	
	
	public Ruta(int ruta, String calle) {
		
		this.ruta = ruta;
		this.calle = calle;
	}



	public int getRuta() {
		return ruta;
	}



	public void setRuta(int ruta) {
		this.ruta = ruta;
	}



	public String getCalle() {
		return calle;
	}



	public void setCalle(String calle) {
		this.calle = calle;
	}
	
	
	
	

}
